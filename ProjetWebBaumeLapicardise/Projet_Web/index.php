<h1> Présentation du site </h1></br>
<p> Bienvenue sur la page d'accueil de ce magnifique site web e-commerce !</p>
<h3> Ce site est divisé en plusieurs parties : </h3>
Une page d'accueil présentant le site. <br>
Un menu facilitant la navigation dans le site. <br>
Une page "A propos" décrivant le travail réalisé et précisant la liste des auteurs du site (notre binôme), et nos difficultés rencontrées. <br>
Un ensemble de pages constituant un catalogue et permettant de parcourir le contenu de la base (par exemple : un lien qui à partir d'une initiale permet d'accéder aux oeuvres d'un compositeur, puis aux albums contenant des enregistrements de ces oeuvres, et enfin aux enregistrement eux-mêmes). <br>
Chaque fois que c'est pertinent, affichage de la photo des musiciens ou de la pochette d'un album, affichage d'un contrôle permettant d'écouter l'extrait de l'enregistrement concerné. <br>
Depuis la page décrivant un album, un accès aux services Amazon affichant les informations sur l'album (détails, prix, ...) grâce à la valeur contenue dans le champ ASIN de la table Album (en utilisant l'API Amazon ), ou la liste des albums semblables disponibles lorsque ce champ n'est pas renseigné (pas un simple renvoi sur une page !).  <br>
Une zone sécurisée (donc avec connexion et suivi de session) permettant de construire un panier d'achat. <br>
